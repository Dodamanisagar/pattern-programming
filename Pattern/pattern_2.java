public class pattern_2 {
    public static void main(String[] args) {
        int n=5;
        for(int i=1;i<=n;i++)//outer loop for------>row
        {
            for(int j=1;j<=n;j++)//inner loop for----->columns
              {  System.out.print("* ");//space is mandetory
               }        
                System.out.println();
        }
    }
    
}
