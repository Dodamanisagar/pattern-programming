public class pattern_4 {
    public static void main(String[] args) {
        int n=5;
        for(int i=1;i<=n;i++)//try i=0
        {
            for(int j=1;j<=n;j++)//try j=0
            {
                System.out.print(i+j+" ");
            }
            System.out.println();
        }
    }
    
}
